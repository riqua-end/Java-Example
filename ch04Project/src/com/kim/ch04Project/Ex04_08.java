package com.kim.ch04Project;

public class Ex04_08 {

	public static void main(String[] args) {
		
		//기본 for문 사용
		for(int i = 1; i <= 3; i++) {
			//초기화부 ; 조건부 ; 증감부
			//1.초기화부의 값으로 조건부에서 비교 true이면 블록안으로 감
			//2.블록의 마지막 문장을 만나면 증감부로 가서 1 증감
			//3.조건부로 가서 비교해서 true이면 블록
			//2번 3번 반복
			//조건부 false이면 for문을 종료
			System.out.println("hello");
		}

	}

}
