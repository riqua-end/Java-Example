package com.kim.secondProject;

public class Ex02_04 {

	public static void main(String[] args) {
		
		int x = 5;
		//변수의 선언과 초기값 대입을 한번에 처리
		System.out.println(x);
		//print문에는 인자값을 변수로 사용하면 변수가 가진값을 출력
		
		x = 10;
		//선언된 변수의 사용시는 변수명 = 값;으로 변수 타입 필요 없음
		System.out.println(x);

	}

}
